import test from 'ava';

import { big64 , get64 } from '../../src' ;

function macro (t, a, o, expected) {
	expected = get64(...expected);
	t.deepEqual(big64(a, o), expected);
}

macro.title = (providedTitle, a, o, expected) => `${providedTitle} big64(${a}, ${o}) === ${expected}`.trim();

test(macro, [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00], 0, [0x00000000, 0x00000000]);
test(macro, [0x00, 0x00, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00], 0, [0x0000FF00, 0x00000000]);
test(macro, [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00], 0, [0x00000000, 0x00000100]);
test(macro, [0x00, 0x00, 0x00, 0xA0, 0x00, 0x00, 0x00, 0x00], 0, [0x000000A0, 0x00000000]);
test(macro, [0x00, 0x00, 0x00, 0xA0, 0x00, 0x00, 0x00, 0x00, 0x03, 0x0C], 1, [0x0000A000, 0x00000003]);
